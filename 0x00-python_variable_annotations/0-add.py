#!/usr/bin/env python3
"""Module to add two floats"""


def add(a: float, b: float) -> float:
    """
    Add two floats and return the result.

    Args:
        a (float): The first float number.
        b (float): The second float number.

    Returns:
        float: The sum of the two input floats.
    """
    return a + b
